const add = (x,y) =>{
  return (x+y)
}
const subtract = (x,y) => {
  return (x-y)
}
const multiply = (x,y) => {
  return (x*y)
}
const divide = (x,y) => {
  return (x/y)
}
const mod = (x,y) => {
  return (x%y)
}
const averageOfTwo = (x,y) => {
  return ((x+y)/2)
}
const averageOfThree = (x,y,z) => {
  return ((x+y+z)/3)
}
const distance= (a,b) => {
  return Math.abs(a-b)
}